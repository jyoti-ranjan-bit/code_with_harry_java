public class practice_set_5_q2 {
    public static void main(String[] args) {
        int sum=0;
        int n=4;
        int i=0;
        while(i<n){
            sum=sum+(2*i);
            i++;
        }
        System.out.println("the sum of first n even nos are:");
        System.out.println(sum);
    }
}
